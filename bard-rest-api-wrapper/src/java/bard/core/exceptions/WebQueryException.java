package bard.core.exceptions;


public class WebQueryException extends RuntimeException {

    public WebQueryException() {
        super();
    }

    public WebQueryException(java.lang.String s) {
        super(s);
    }

    public WebQueryException(java.lang.String s, java.lang.Throwable throwable) {
        super(s, throwable);
    }

    public WebQueryException(java.lang.Throwable throwable) {
        super(throwable);
    }

}
